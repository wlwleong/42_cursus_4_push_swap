#include "libft.h"

int	ft_memcmp(const void *buf1, const void *buf2, size_t count)
{
	size_t	i;
	char	*str1;
	char	*str2;

	str1 = (char *)buf1;
	str2 = (char *)buf2;
	i = 0;
	while (i < count)
	{
		if (str1[i] != str2[i])
			return ((unsigned char)str1[i] - (unsigned char)str2[i]);
		i++;
	}
	return (0);
}
